public class Book
{
    public string Title;
    public string Description;
    public string Category;
    public string Format;
    public string Author;
    public string Genre;

    public Book(string title, string desc, string category, string format, string author, string genre)
    {
        Title = title;
        Description = desc;
        Category = category;
        Format = format;
        Author = author;
        Genre = genre;
    }
}
